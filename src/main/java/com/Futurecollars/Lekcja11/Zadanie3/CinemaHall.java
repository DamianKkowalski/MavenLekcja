package com.Futurecollars.Lekcja11.Zadanie3;

public class CinemaHall {
    private String name;
    private String type;

    public CinemaHall(String name, String type)
    {
        this.name = name;
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }
}
